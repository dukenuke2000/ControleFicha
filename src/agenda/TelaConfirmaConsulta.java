    public TelaConfirmaConsulta(JTable tblAgendas, Calendar dataConsulta, String idMedico, int idPaciente) {
        initComponents();
        
        this.dataConsulta = dataConsulta;
        
        this.tblAgendas = tblAgendas;
        
        objAgendaConsulta = new agendaConsulta();
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        String HoraAgendada = tblAgendas.getValueAt(tblAgendas.getSelectedRow(), 1).toString();
        
        idHoraAgenda = Integer.parseInt(tblAgendas.getValueAt(tblAgendas.getSelectedRow(), 0).toString());
        this.idPaciente = idPaciente;
        this.idMedico = Integer.parseInt(idMedico);
        
        lblHora.setText(sdf.format(dataConsulta.getTime()) + " - " + HoraAgendada);
        
    }

